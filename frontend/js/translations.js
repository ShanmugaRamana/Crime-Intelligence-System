// ─── Heatmap Color Scale ────────────────────────────────────
function getHeatmapColor(value, max) {
    if (value === 0 || max === 0) return '#f5f5f5';
    const intensity = value / max;
    if (intensity < 0.25) return '#bbdefb';
    if (intensity < 0.5) return '#64b5f6';
    if (intensity < 0.75) return '#1e88e5';
    return '#0d47a1';
}

function getHeatmapTextColor(value, max) {
    if (value === 0 || max === 0) return '#999';
    const intensity = value / max;
    return intensity >= 0.5 ? '#fff' : '#333';
}
